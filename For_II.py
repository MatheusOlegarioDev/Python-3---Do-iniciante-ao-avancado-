# Estrutura FOR

lista = [1,2,4,5,6,7]

for num in lista:
    if num % 2 == 0:
        print(num, 'Par')
    else:
        print(num, 'Impar')

soma = 0

for num in lista:
    soma += num
print('Somatório da lista:', num)

nome = 'Matheus Olegário'

for char in nome:
    print(char)


